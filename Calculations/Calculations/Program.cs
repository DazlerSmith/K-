using System;

namespace Calculations
{
    class Program
    {
        static void Main(string[] args)
        {
            // Declare Variables
            int a = 8;
            int b = 6;
            int c;

            c = a * b; // This will add A and B (4 and 6)

            //Show the values on the screen
            Console.WriteLine("A is " + a);
            Console.WriteLine("B is " + b);
            Console.WriteLine("A * B is " + c);
            Console.ReadLine(); // Stops the program
        }
    }
}
